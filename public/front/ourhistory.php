                            <p>
                                Respublika Dövlət Uşaq Kitabxanası 1965-ci il yanvarın 1-də Azərbaycan Respublikası Nazirlər Sovetinin qərarı ilə M.Qorki adına kütləvi kitabxana fondunun bazası əsasında yaradılmışdır. 12 sentyabr 1967-ci ildən Azərbaycan Respublikası Mərkəzi Komitəsinin 25 saylı (§22) sərəncamına əsasən uşaq ədəbiyyatının inkişafında mühüm xidmətləri olan, Azərbaycan ədəbi dilinin saflığı uğrunda daim mübarizə aparmış Azərbaycan ədəbiyyatşünası, pedaqoq, maarifpərvər, uşaq folklor antologiyasının banisi <a href="firudin.php">Firidun bəy Köçərli</a> nin adını daşıyır.
                                5 şöbə-xidmət, kitabsaxlayıcı, komplektləşdirmə, metodika və mübadilə fondu ilə fəaliyyətə başlayan kitabxananın ilk direktoru Nemət Əliyev olmuşdur.
                                Kitabxana yarandığı dövrdə fondu cəmi 43000 nüsxə ədəbiyyatdan ibarət idi, onun 27337 nüsxəsini uşaq ədəbiyyatı təşkil edirdi.
                                60-cı illərin əvvəllərində Azərbaycanda 88, Bakı şəhərində 22 müstəqil uşaq kitabxanası fəaliyyət göstərirdi. Bu illərdə uşaq kitabxanalarına metodik rəhbərlik mahiyyət etibarı ilə vahid və bütöv sistem kimi təşəkkül tapmamışdı.
                                Respublika Uşaq Kitabxanasının yaradılması uşaqlara kitabxana xidmətinin və onun metodik təminat sisteminin formalaşmasında irəliyə atılmış ciddi addım oldu. Belə ki, F.Köçərli adına Respublika Uşaq Kitabxanasının yaradılması nəticəsində uşaqlara kitabxana xidmətinin ümumrespublika metodik mərkəzi meydana gəldi. Uşaqlara xidmət edən kitabxanalara, o cümlədən, 3 mindən çox məktəb kitabxanasına metodik rəhbərlik təmin edildi. Nəhayət, uşaq kitabxanaları üçün metodik mərkəzin yaradılması bu işə xidmət edən kitabxanaların fəaliyyətinin əlaqələndirilməsinə və tam bir sistem kimi fəaliyyət göstərməsinə hüquqi, nəzəri, metodik və təşkilatı zəmin yaratdı.
                                Respublika Uşaq Kitabxanasının elmi-metodik fəaliyyətini kitabxanaşünaslıq elminin son nailiyyətləri zəminində təşkil etmək məqsədilə 60-ci illərin sonu və 70-ci illərin əvvəlində zəngin metodik nəşrlər fondu yaradıldı. Fondda uşaqlara kitabxana xidmətinin bütün nəzəri, metodik və təcrübi nailiyyətlərini və qabaqcıl təcrübəni əks etdirən zəngin ədəbiyyat toplandı.
                                Respublika Uşaq Kitabxanası kitabxanaşünaslıq, biblioqrafiyaşünaslıq, kitabşünaslıq sahəsində aparıcı müəssisə, respublikada bütün uşaq kitabxanaları üçün elmi-metodiki mərkəz, oxuculara kitabxana-biblioqrafiya, informasiya xidməti göstərmək, kitabxanalararası abonement, tövsiyə biblioqrafiyası funksialarını yerinə yetirir. Respublika Uşaq Kitabxanası yarandığı gündən geniş sosial-mədəni fəaliyyətlə yanaşı gigər qrum, təşkilat və qeyri-dövlət təşkilatları ilə birgə tədbirlərin keçiriməsinə təşəbbüs göstərir.
                                1990-cı ildə kitabxananın nəzdində “Körpəm” eksperimental uşaq teatr studiyası yaradılmış və “Körpəm” adlı uşaq jurnalı təsis edilmiş, 2006-cı ildən etibarən jurnalın adı dəyişdirilərək “Göy qurşağı” adı ilə nəşr olunur.
                                1995-2005- ci illərdə ildə AzTV–nin “Çıraq”, “Sərbəst düşüncə” uşaq verilişlərinin çəkilişləri kitabxananın oxu zalında aparılıb.
                                1999-cu ildən Respublika Uşaq Kitabxanası Azərbaycan Kitabxanaçılar Cəmiyyətinin üzvüdür.
                                2002-ci ildə kitabxananın <a href="www.childlibbaku.org">www.childlibbaku.org</a> domeni adı ilə rəsmi veb saytı fəaliyyətə başlamış, 2006-cı ildə domen adı dəyişilərək www.clb.az olmuşdur.
                                Azərbaycan Respublikasının Prezidenti cənab İlham Əliyevin imzaladığı 2004-cü il 12 yanvar tarixli “Azərbaycan dilində latın qrafikası ilə kütləvi nəşrlərin həyata keçirilməsi haqqında”, 2004-cü il 27 dekabr tarixli “2005-2006-cı illərdə Azərbaycan dilində latın qrafikası ilə çapı nəzərdə tutulan əsərləri siyahısının təsdiq edilməsi haqqında” sərəncamlar kitabxana işinin inkişafında yeni bir mərhələ oldu.
                                2004-cü ildə “Mədəni-Maarif” jurnalında Respublika Uşaq Kitabxanasının təklifi ilə “Uşaq kitabxanalarına kömək” rubrikası açılmış, elmi-metodika şöbəsi tərəfindən tərtib edilmiş metodik vəsaitlər jurnalın hər nömrəsində işıqlandırılmışdır.
                                2005-ci ildən F.Köçərli adına Respublika Uşaq Kitabxanası fondun bütünlüklə AKİS (Avtomatlaşdırılmış kitabxana-informasiya sistemi) proqram təmitanından istifadə edərək elektron informasiya daşıyıcılarına köçürülməsi və elektron kataloqun yaradılması proseslərini müvəffəqiyyətlə həyata keçirir.
                                2008-ci il 6 oktyabr tarixli Azərbaycan Respublikasının Prezidenti cənab İlham Əliyevin imzaladığı “Azərbaycan Respublikasında kitabxana-informasiya sahəsinin 2008-2013-cü illərdə inkişafı üzrə Dövlət Proqramı”nın təsdiq edilməsi barədə sərəncam kitabxana işinin inkişafında yeni bir mərhələyə başlanğıc verdi.
                                2009-cu il "Uşaq İli" ilə əlaqədar “Dünya uşaq ədəbiyyatının inciləri” adlı Beynəlxalq kitab sərgisində ölkəmizdə fəaliyyət göstərən iyirmi dövlətin səfirliklərinin nümayəndələri tərəfindən 1500-dən çox kitab Respublika Uşaq Kitabxanasına hədiyyə edilib.
                                2010-cu ildə Respublika Uşaq Kitabxanası müasir tələblərə cavab verən yeni binaya köçüb.
                                2011-ci ildə Mədəniyyət və Turizm Nazirliyinin təşəbbüsü ilə Bosniya Hersoqovinanın “Dervis Susic” Kütləvi və Universitet kitabxanası ilə F.Köçərli adına Respublika Uşaq Kitabxanası “Qardaşlaşmış Kitabxanalar” elan olunub. Bunun nəticəsi olaraq Beynəlxalq Kitabxanalararası abonement bölməsi İ.Qasprinski adına Krım Tatar Kitabxanası, İon Kryange adına Moldova Milli Uşaq Kitabxanası, Ukrayna Milli Uşaq Kitabxanası, Serbiya kitabxanası, Türkiyənin bir çox kitabxanaları, Rusiyanın Dövlət Uşaq Kitabxanası, İsveçin Göteborq şəhər Kitabxanası və Stokholm Beynəlxalq kitabxanası ilə də beynəlxalq əlaqələr yaratmış, beynəlxalq kitab sərgilərində iştirak etmişdir.
                                2012-ci ildən F. Köçərli adına Respublika Uşaq Kitabxanası Beynəlxalq Uşaq Kitabı Şurasının (İBBY) üzvüdür.
                                2013-cü ildən F. Köçərli adına Respublika Uşaq Kitabxanası “Uşaq ədəbiyyatı xəzinəsindən” adlı seriyadan <a href="">“Firidun bəy Köçərli”</a>, <a href="">“İlyas Tapdıq”</a>, <a href="">“Abdulla Şaiq”</a>, <a href="">“Abbas Səhhət”</a>, <a href="">“Mir Mehdi Seyidzadə”</a>, <a href="">“Tofiq Mahmud”</a>, <a href="">“Xanımana Əlibəyli”</a>, <a href="">“Mikayıl Rzaquluzadə”</a> biblioqrafik kitabları nəşr edir.
                                2013-cü ildə Respublika Uşaq Kitabxanasının təşəbbüsü ilə Firidun bəy Köçərlinin <a href="firidun-book.php">“Balalara hədiyyə”</a>, 2017-ci ildə <a href="">“Türk dünyasının nağıl bağı”</a>, 2018-ci ildə <a href="">“Əsrarəngiz Misir nağılları”</a> kitabları nəşr edilmişdir.
                                2014-cü ildə Azərbaycan Respublikası Mədəniyyət və Turizm Nazirliyi, Əmək və Əhalinin Sosial Müdafiəsi Nazirliyinin və Gənc Ana və Uşaqların Maarifləndirilməsi İctimai Birliyinin dəstəyi, F.Köçərli adına Respublika Uşaq Kitabxanası və TİKA-nın Bakı ofisinin tərəfdaşlığı çərçivəsində görmə qabiliyyəti zəif və məhdud uşaqlar üçün Dil kursları və İnformasiya mərkəzi açıldı. Bu layihənin həyata keçməsi görmə məhdudyyiətli uşaqların informasiya almaq hüququnu reallaşdırdı.
                                2015-ci ildə Firidun bəy Köçərli adına Respublika Uşaq Kitabxanasının 50 illik yubileyi münasibətilə, Azərbaycanda kitabxana işinin təşkili sahəsində səmərəli fəaliyyətlərinə görə F.Köçərli adına Respublika Uşaq Kitabxanasının bir qrup əməkdaşları ölkə prezidenti cənab İlham Əliyevin tərəfindən <a href="https://president.az/articles/17234">“Əməkdar mədəniyyət işçisi”</a> fəxri adına və <a href="https://president.az/articles/17235?locale=en">“Tərəqqi”</a> medalına layiq görülmüşlər.  

